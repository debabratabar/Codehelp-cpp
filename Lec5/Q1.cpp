#include<iostream>
using namespace std;

int main() {

    // bitwise operator
    int a =2,b=4;
    cout << "a&b=" << (a&b) << endl;
    cout << "~a=" << (~a) << endl;
    cout << "a|b=" << (a|b) << endl;
    cout << "a^b=" << (a^b) << endl;

    // right shift & left shift

    cout << "17<<1= " << (17<<1) << endl;
    cout << "17<<2= " << (17<<2) << endl;
    cout << "17>>1= " << (17>>1) << endl;
    cout << "17>>2= " << (17>>2) << endl;

}